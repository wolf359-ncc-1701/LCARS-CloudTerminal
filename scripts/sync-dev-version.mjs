import { promises as fs } from "node:fs";
import path from "node:path";

const root = process.cwd();
const configPath = path.join(root, "config", "dev-version.json");
const versionConfig = JSON.parse(await fs.readFile(configPath, "utf8"));
const version = versionConfig.version;
const label = versionConfig.label || `DEV V.${version}`;
const codename = versionConfig.codename || "LCARS Development";

const tsOutput = `export const DEV_VERSION = "${version}";\nexport const DEV_LABEL = "${label}";\nexport const DEV_CODENAME = "${codename}";\n`;
await fs.writeFile(path.join(root, "src", "app", "devVersion.ts"), tsOutput, "utf8");

const mdOutput = `# Current DEV Baseline\n\n- Version: V${version}\n- Label: ${label}\n- Codename: ${codename}\n\nThis file is generated by \`npm run sync:dev\`. Task files and UI should reference this baseline instead of hardcoding older DEV labels.\n`;
await fs.writeFile(path.join(root, "docs", "current-dev.md"), mdOutput, "utf8");

console.log(`Synced ${label} (${codename})`);
